test("fio: seletor Sim/Não liga e desliga a seta", async ({ page }) => {
  await gotoEditor(page);
  await loadFigures(page, [moldRect("mold_fio", { name: "Frente" })]);
  await selectAt(page, MOLD_CX, MOLD_CY);

  const fio = page.getByTestId("mold-grainline-enabled");
  await expect(fio).toHaveValue("nao");
  await expect.poll(() => countByName(page, "inaa-mold-grainline")).toBe(0);

  // Sim → arrow appears. The mold is taller than wide, so the auto heuristic
  // picks 90°.
  await fio.selectOption("sim");
  await expect.poll(() => countByName(page, "inaa-mold-grainline")).toBe(1);
  await expect
    .poll(async () => (await getFigures(page))[0]?.moldMeta?.grainline?.angleDeg)
    .toBe(90);

  // Não → arrow gone, grainline cleared.
  await fio.selectOption("nao");
  await expect.poll(() => countByName(page, "inaa-mold-grainline")).toBe(0);
  await expect
    .poll(async () => (await getFigures(page))[0]?.moldMeta?.grainline ?? null)
    .toBeNull();
});

test("girar os textos (transform interno) não move a seta do fio", async ({
  page,
}) => {
  await gotoEditor(page);
  await loadFigures(page, [
    moldRect("mold_rot", {
      name: "Frente",
      moldMeta: { grainline: { angleDeg: 30 } },
    }),
  ]);
  await selectAt(page, MOLD_CX, MOLD_CY);

  await expect.poll(() => countByName(page, "inaa-mold-grainline")).toBe(1);
  const grainBefore = await page.evaluate(
    () =>
      window.__INAA_DEBUG__?.getStageNodePointsByName?.("inaa-mold-grainline") ??
      []
  );

  // Enter the doc block's inner transform mode and rotate it ~30°.
  const stage = page.getByTestId("editor-stage-container");
  const box = await stage.boundingBox();
  const handles = await page.evaluate(
    () =>
      window.__INAA_DEBUG__?.getStageNodeAbsolutePositionsByName?.(
        "inaa-figure-name-handle"
      ) ?? []
  );
  await page.mouse.dblclick(box!.x + handles[0]!.x, box!.y + handles[0]!.y);
  await expect
    .poll(() => countByName(page, "inaa-inner-proxy-doc"))
    .toBe(1);

  // Poll for the INNER transformer's rotater near the block (right after
  // entering the mode the anchors may not be positioned yet, and the detached
  // outer transformer parks its anchors far away).
  let rotater: { x: number; y: number } | null = null;
  await expect
    .poll(async () => {
      const rects = await page.evaluate(
        () =>
          window.__INAA_DEBUG__?.getStageNodeClientRectsByName?.("rotater") ??
          []
      );
      // Reverse: the inner transformer mounts after the outer one, whose
      // detached anchors keep stale positions that can also fall in range.
      for (const r of [...rects].reverse()) {
        const c = { x: r.x + r.width / 2, y: r.y + r.height / 2 };
        if (Math.hypot(c.x - MOLD_CX, c.y - MOLD_CY) <= 200) {
          rotater = c;
          return true;
        }
      }
      return false;
    })
    .toBe(true);
  const start = {
    x: box!.x + rotater!.x,
    y: box!.y + rotater!.y,
  };
  const pivot = { x: box!.x + MOLD_CX, y: box!.y + MOLD_CY };
  const rad = (30 * Math.PI) / 180;
  const end = {
    x: pivot.x + (start.x - pivot.x) * Math.cos(rad) - (start.y - pivot.y) * Math.sin(rad),
    y: pivot.y + (start.x - pivot.x) * Math.sin(rad) + (start.y - pivot.y) * Math.cos(rad),
  };
  await page.mouse.move(start.x, start.y);
  await page.mouse.down();
  await page.mouse.move(end.x, end.y, { steps: 8 });
  await page.mouse.up();

  await expect
    .poll(async () => Math.abs(((await getFigures(page))[0]?.nameRotationDeg ?? 0) - 30))
    .toBeLessThan(8);

  // Grain arrow direction is driven by the fio value, not the text rotation.
  const grainAfter = await page.evaluate(
    () =>
      window.__INAA_DEBUG__?.getStageNodePointsByName?.("inaa-mold-grainline") ??
      []
  );
  expect(JSON.stringify(grainAfter)).toBe(JSON.stringify(grainBefore));
});

test("a seta do fio não sobrepõe o bloco de documentação", async ({ page }) => {
  await gotoEditor(page);
  await loadFigures(page, [
    moldRect("mold_no_overlap", {
      name: "Frente",
      moldMeta: {
        baseSize: "M",
        cutQuantity: 2,
        cutOnFold: true,
        grainline: { angleDeg: 30 },
      },
    }),
  ]);
  await selectAt(page, MOLD_CX, MOLD_CY);

  await expect.poll(() => countByName(page, "inaa-mold-doc-line")).toBe(4);
  await expect.poll(() => countByName(page, "inaa-mold-grainline")).toBe(1);

  const rects = await page.evaluate(() => {
    const get = (n: string) =>
      window.__INAA_DEBUG__?.getStageNodeClientRectsByName?.(n) ?? [];
    return {
      doc: get("inaa-mold-doc"),
      arrow: [
        ...get("inaa-mold-grainline"),
        ...get("inaa-mold-grainline-head"),
      ],
    };
  });
  expect(rects.doc).toHaveLength(1);
  expect(rects.arrow).toHaveLength(2);

  const doc = rects.doc[0]!;
  for (const r of rects.arrow) {
    const separated =
      r.x + r.width <= doc.x ||
      doc.x + doc.width <= r.x ||
      r.y + r.height <= doc.y ||
      doc.y + doc.height <= r.y;
    expect(separated).toBe(true);
  }
});

test("arrastar o handle move o bloco de documentação sem mover o molde", async ({
  page,
}) => {
  await gotoEditor(page);
  await loadFigures(page, [moldRect("mold_handle", { name: "Frente" })]);
  await selectAt(page, MOLD_CX, MOLD_CY);

  await expect
    .poll(() => countByName(page, "inaa-figure-name-handle"))
    .toBe(1);

  const positions = await page.evaluate(
    () =>
      window.__INAA_DEBUG__?.getStageNodeAbsolutePositionsByName?.(
        "inaa-figure-name-handle"
      ) ?? []
  );
  expect(positions).toHaveLength(1);

  const stage = page.getByTestId("editor-stage-container");
  const box = await stage.boundingBox();
  expect(box).not.toBeNull();
  // The handle rect is center-anchored (x/y + offset), so its absolute
  // position IS its visual center — aim straight at it.
  const startX = box!.x + positions[0]!.x;
  const startY = box!.y + positions[0]!.y;

  await page.mouse.move(startX, startY);
  await page.mouse.down();
  await page.mouse.move(startX + 40, startY + 30, { steps: 5 });
  await page.mouse.up();

  const fig = (await getFigures(page))[0]!;
  // The documentation block followed the pointer...
  expect(Math.abs((fig.nameOffsetLocal?.x ?? 0) - 40)).toBeLessThan(3);
  expect(Math.abs((fig.nameOffsetLocal?.y ?? 0) - 30)).toBeLessThan(3);
  // ...and the mold itself stayed put (regression: stage direct-drag used to
  // move the figure together with the handle).
  expect(fig.x ?? 0).toBe(0);
  expect(fig.y ?? 0).toBe(0);
});

test("a seta do fio tem handle próprio que move só a seta", async ({
  page,
}) => {
  await gotoEditor(page);
  await loadFigures(page, [
    moldRect("mold_grain_drag", {
      name: "Frente",
      moldMeta: { grainline: { angleDeg: 0 } },
    }),
  ]);
  await selectAt(page, MOLD_CX, MOLD_CY);

  await expect.poll(() => countByName(page, "inaa-grain-handle")).toBe(1);
  await expect
    .poll(() => countByName(page, "inaa-figure-name-handle"))
    .toBe(1);

  const grainPointsBefore = await page.evaluate(
    () =>
      window.__INAA_DEBUG__?.getStageNodePointsByName?.(
        "inaa-mold-grainline"
      )?.[0] ?? []
  );
  expect(grainPointsBefore.length).toBe(4);

  // Drag the grain handle by (+60, +50). The handle rect is center-anchored,
  // so its absolute position is its visual center.
  const positions = await page.evaluate(
    () =>
      window.__INAA_DEBUG__?.getStageNodeAbsolutePositionsByName?.(
        "inaa-grain-handle"
      ) ?? []
  );
  expect(positions).toHaveLength(1);
  const stage = page.getByTestId("editor-stage-container");
  const box = await stage.boundingBox();
  const startX = box!.x + positions[0]!.x;
  const startY = box!.y + positions[0]!.y;
  await page.mouse.move(startX, startY);
  await page.mouse.down();
  await page.mouse.move(startX + 60, startY + 50, { steps: 5 });
  await page.mouse.up();

  // The arrow moved by the drag delta…
  const grainPointsAfter = await page.evaluate(
    () =>
      window.__INAA_DEBUG__?.getStageNodePointsByName?.(
        "inaa-mold-grainline"
      )?.[0] ?? []
  );
  expect(grainPointsAfter.length).toBe(4);
  for (let i = 0; i < 4; i += 2) {
    expect(Math.abs(grainPointsAfter[i]! - grainPointsBefore[i]! - 60)).toBeLessThan(3);
    expect(
      Math.abs(grainPointsAfter[i + 1]! - grainPointsBefore[i + 1]! - 50)
    ).toBeLessThan(3);
  }

  // …the offset persisted, and neither the mold nor the text block moved.
  const fig = (await getFigures(page))[0]!;
  expect(Number.isFinite(fig.moldMeta?.grainOffsetLocal?.x ?? NaN)).toBe(true);
  expect(Number.isFinite(fig.moldMeta?.grainOffsetLocal?.y ?? NaN)).toBe(true);
  expect((fig as { x?: number }).x ?? 0).toBe(0);
  expect((fig as { y?: number }).y ?? 0).toBe(0);
  expect(fig.nameOffsetLocal ?? { x: 0, y: 0 }).toEqual({ x: 0, y: 0 });
});

