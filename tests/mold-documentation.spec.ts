// Minimal figure shapes for loadTestProject. Extra fields (name/moldMeta) are
// spread through verbatim by the editor's loadProject.
type TestNode = { id: string; x: number; y: number; mode: "corner" };
type TestFigure = {
  id: string;
  tool: "line";
  kind?: "mold";
  name?: string;
  x: number;
  y: number;
  rotation: number;
  closed: boolean;
  nodes: TestNode[];
  edges: Array<{ id: string; from: string; to: string; kind: "line" }>;
  stroke: string;
  strokeWidth: number;
  fill: string;
  opacity: number;
  nameFontSizePx?: number;
  nameRotationDeg?: number;
  moldMeta?: {
    baseSize?: string;
    cutQuantity?: number;
    cutOnFold?: boolean;
    notes?: string;
    docFontSizePx?: number;
    visible?: boolean;
    printEnabled?: boolean;
    grainline?: { angleDeg: number; autoGenerated?: boolean };
  };
};

type FigureSnapshot = {
  id: string;
  kind?: string;
  name?: string;
  x?: number;
  y?: number;
  nameFontSizePx?: number;
  nameRotationDeg?: number;
  nameOffsetLocal?: { x: number; y: number };
  moldMeta?: {
    baseSize?: string;
    cutQuantity?: number;
    cutOnFold?: boolean;
    notes?: string;
    docFontSizePx?: number;
    grainline?: { angleDeg: number };
    grainOffsetLocal?: { x: number; y: number };
  };
};

/** Closed rectangle. World coords == screen coords in a fresh editor. */
function createRect(
  id: string,
  x: number,
  y: number,
  w: number,
  h: number,
  extra?: Partial<TestFigure>
): TestFigure {
  const a = `${id}_a`;
  const b = `${id}_b`;
  const c = `${id}_c`;
  const d = `${id}_d`;
  return {
    id,
    tool: "line",
    x: 0,
    y: 0,
    rotation: 0,
    closed: true,
    nodes: [
      { id: a, x, y, mode: "corner" },
      { id: b, x: x + w, y, mode: "corner" },
      { id: c, x: x + w, y: y + h, mode: "corner" },
      { id: d, x, y: y + h, mode: "corner" },
    ],
    edges: [
      { id: `${id}_e1`, from: a, to: b, kind: "line" },
      { id: `${id}_e2`, from: b, to: c, kind: "line" },
      { id: `${id}_e3`, from: c, to: d, kind: "line" },
      { id: `${id}_e4`, from: d, to: a, kind: "line" },
    ],
    stroke: "aci7",
    strokeWidth: 2,
    fill: "rgba(96,165,250,0.22)",
    opacity: 1,
    ...extra,
  };
}

async function loadFigures(
  page: import("@playwright/test").Page,
  figures: TestFigure[]
) {
  await page.evaluate((payload) => {
    window.__INAA_DEBUG__?.loadTestProject?.({ figures: payload });
  }, figures);
  await expect
    .poll(async () =>
      page.evaluate(() => window.__INAA_DEBUG__?.getState?.().figuresCount ?? 0)
    )
    .toBe(figures.length);
}

async function getFigures(
  page: import("@playwright/test").Page
): Promise<FigureSnapshot[]> {
  return page.evaluate(
    () =>
      (window.__INAA_DEBUG__?.getFiguresSnapshot?.() ?? []) as FigureSnapshot[]
  );
}

async function selectAt(
  page: import("@playwright/test").Page,
  x: number,
  y: number
) {
  const stage = page.getByTestId("editor-stage-container");
  await expect(stage).toBeVisible();
  await stage.click({ position: { x, y } });
  await expect
    .poll(async () => (await getEditorState(page)).selectedFigureId)
    .not.toBeNull();
}

async function countByName(
  page: import("@playwright/test").Page,
  name: string
): Promise<number> {
  return page.evaluate(
    (n) => window.__INAA_DEBUG__?.countStageNodesByName?.(n) ?? 0,
    name
  );
}

// A tall rect mold centered at (300, 280).
function moldRect(id: string, extra?: Partial<TestFigure>): TestFigure {
  return createRect(id, 220, 180, 160, 200, { kind: "mold", ...extra });
}
const MOLD_CX = 300;
const MOLD_CY = 280;

test("conversão de figura em molde copia o nome existente", async ({
  page,
}) => {
  await gotoEditor(page);
  const stage = page.getByTestId("editor-stage-container");
  await expect(stage).toBeVisible();

  await loadFigures(page, [
    createRect("fig_named", 200, 200, 140, 100, { name: "Frente" }),
  ]);

  await stage.click({ button: "right", position: { x: 260, y: 250 } });
  await expect(page.getByTestId("edge-context-menu")).toBeVisible();
  await page.getByTestId("edge-context-convert-figure-to-mold").click();
  await expect(page.getByTestId("edge-context-menu")).toHaveCount(0);

  await expect
    .poll(async () => {
      const f = (await getFigures(page))[0];
      return f ? `${f.kind}:${f.name}` : null;
    })
    .toBe("mold:Frente");
});

